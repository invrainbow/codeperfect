bool search_ranges(int c, Unicode_Range *ranges, int count) {
    int lo = 0, hi = count-1;

    if (c < ranges[0].start || c > ranges[hi].end)
        return false;

    while (lo <= hi) {
        auto mid = (lo+hi)/2;
        Unicode_Range *it = ranges + mid;

        if (c > it->end)
            lo = mid + 1;
        else if (c < it->start)
            hi = mid - 1;
        else
            return (it->stride == 1 || (c - it->start) % it->stride == 0);
    }
    return false;
}

bool uni_isalpha(int c) {
    if (c < 128)
        return isalpha(c);
    return search_ranges(c, letter_ranges, _countof(letter_ranges));
}

bool uni_isdigit(int c) {
    if (c < 128)
        return isdigit(c);
    return search_ranges(c, digit_ranges, _countof(digit_ranges));
}

bool uni_isprint(int c) {
    if (c < 128)
        return isprint(c);

    return search_ranges(c, print_range_L, _countof(print_range_L))
        || search_ranges(c, print_range_M, _countof(print_range_M))
        || search_ranges(c, print_range_N, _countof(print_range_N))
        || search_ranges(c, print_range_P, _countof(print_range_P))
        || search_ranges(c, print_range_S, _countof(print_range_S));
}

