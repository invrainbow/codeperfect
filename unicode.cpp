Uni_Property get_codepoint_property(int c) {
    int lo = 0, hi = _countof(property_table) - 1;
    while (lo <= hi) {
        int mid = (lo+hi)/2;
        if (c > property_table[mid].end)
            lo = mid + 1;
        else if (c < property_table[mid].start)
            hi = mid - 1;
        else
            return property_table[mid].prop;
    }
    return PR_ANY;
}

struct Gr_Next {
    Gr_State state;
    bool isbreak;
    int rule;
};

bool grapheme_transition(Gr_State state, Uni_Property prop, Gr_Next *out) {
    switch (state) {
    case GR_ANY:
        switch (prop) {
        case PR_CR: *out = {GR_CR, true, 50}; return true;
        case PR_LF: *out = {GR_CONTROLLF, true, 50}; return true;
        case PR_CONTROL: *out = {GR_CONTROLLF, true, 50}; return true;
        case PR_L: *out = {GR_L, true, 9990}; return true;
        case PR_LV: *out = {GR_LVV, true, 9990}; return true;
        case PR_V: *out = {GR_LVV, true, 9990}; return true;
        case PR_LVT: *out = {GR_LVTT, true, 9990}; return true;
        case PR_T: *out = {GR_LVTT, true, 9990}; return true;
        case PR_EXTEND: *out = {GR_ANY, false, 90}; return true;
        case PR_ZWJ: *out = {GR_ANY, false, 90}; return true;
        case PR_SPACINGMARK: *out = {GR_ANY, false, 91}; return true;
        case PR_PREPREND: *out = {GR_PREPEND, true, 9990}; return true;
        case PR_EXTENDEDPICTOGRAPHIC: *out = {GR_EXTENDEDPICTOGRAPHIC, true, 9990}; return true;
        case PR_REGIONALINDICATOR: *out = {GR_RIODD, true, 9990}; return true;
        }
        break;
    case GR_CR:
        switch (prop) {
        case PR_ANY: *out = {GR_ANY, true, 40}; return true;
        case PR_LF: *out = {GR_ANY, false, 30}; return true;
        }
        break;
    case GR_CONTROLLF:
        switch (prop) {
        case PR_ANY: *out = {GR_ANY, true, 40}; return true;
        }
        break;
    case GR_L:
        switch (prop) {
        case PR_LVT: *out = {GR_LVTT, false, 60}; return true;
        case PR_LV: *out = {GR_LVV, false, 60}; return true;
        case PR_L: *out = {GR_L, false, 60}; return true;
        case PR_V: *out = {GR_LVV, false, 60}; return true;
        }
        break;
    case GR_LVV:
        switch (prop) {
        case PR_T: *out = {GR_LVTT, false, 70}; return true;
        case PR_V: *out = {GR_LVV, false, 70}; return true;
        }
        break;
    case GR_LVTT:
        switch (prop) {
        case PR_T: *out = {GR_LVTT, false, 80}; return true;
        }
        break;
    case GR_PREPEND:
        switch (prop) {
        case PR_ANY: *out = {GR_ANY, false, 92}; return true;
        }
        break;
    case GR_EXTENDEDPICTOGRAPHIC:
        switch (prop) {
        case PR_EXTEND: *out = {GR_EXTENDEDPICTOGRAPHIC, false, 110}; return true;
        case PR_ZWJ: *out = {GR_EXTENDEDPICTOGRAPHICZWJ, false, 110}; return true;
        }
        break;
    case GR_EXTENDEDPICTOGRAPHICZWJ:
        switch (prop) {
        case PR_EXTENDEDPICTOGRAPHIC: *out = {GR_EXTENDEDPICTOGRAPHIC, false, 110}; return true;
        }
        break;
    case GR_RIODD:
        switch (prop) {
        case PR_REGIONALINDICATOR: *out = {GR_RIEVEN, false, 120}; return true;
        }
        break;
    case GR_RIEVEN:
        switch (prop) {
        case PR_REGIONALINDICATOR: *out = {GR_RIODD, true, 120}; return true;
        }
        break;
    }

    return false;
}

void Grapheme_Clusterer::init() {
    ptr0(this);
    start = true;
    state = GR_ANY;
}

bool Grapheme_Clusterer::feed(int codepoint) {
    auto nextprop = get_codepoint_property(codepoint);
    bool isbreak = false;

    Gr_Next next; ptr0(&next);
    if (grapheme_transition(state, nextprop, &next)) {
        state = next.state;
        isbreak = next.isbreak;
    } else {
        Gr_Next notfound = {GR_ANY, true, 0};
        Gr_Next *next = &notfound;

        Gr_Next any_prop_next; ptr0(&any_prop_next);
        if (grapheme_transition(state, PR_ANY, &any_prop_next))
            next = &any_prop_next;

        Gr_Next any_state_next; ptr0(&any_state_next);
        if (grapheme_transition(GR_ANY, nextprop, &any_state_next))
            if (any_state_next.rule >= next->rule)
                next = &any_state_next;

        state = next->state;
        isbreak = next->isbreak;
    }

    if (start) {
        start = false;
        isbreak = true;
    }

    return isbreak;
}

