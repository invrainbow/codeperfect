bool _find(int c, Range *ranges, int ranges_len) {
    int lo = 0, hi = ranges_len-1;

    if (c < ranges[0].start || c > ranges[hi].end)
        return false;

    while (lo <= hi) {
        auto mid = (lo+hi)/2;
        if (c > ranges[mid].end)
            lo = mid + 1;
        else if (c < ranges[mid].start)
            hi = mid - 1;
        else
            return true;
    }
    return false;
}

int cp_wcwidth(uchar c) {
    switch (c) {
    case 0:
    case 0x034F:
    case 0x200B:
    case 0x200C:
    case 0x200D:
    case 0x200E:
    case 0x200F:
    case 0x2028:
    case 0x2029:
    case 0x202A:
    case 0x202B:
    case 0x202C:
    case 0x202D:
    case 0x202E:
    case 0x2060:
    case 0x2061:
    case 0x2062:
    case 0x2063:
        return 0;
    }

    if (c < 32 || (0x07F <= c && c < 0x0A0))
        return -1;
    if (_find(c, zero_width, _countof(zero_width)))
        return 0;
    return 1 + (_find(c, east_asian, _countof(east_asian)) ? 1 : 0);
}

int cp_wcswidth(uchar *s, int len) {
    int ret = 0;
    for (int i = 0; i < len; i++) {
        auto width = cp_wcwidth(s[i]);
        if (width < 0) return -1;
        ret += width;
    }
    return ret;
}

